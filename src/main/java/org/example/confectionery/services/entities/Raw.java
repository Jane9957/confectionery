package org.example.confectionery.services.entities;

public class Raw {
    String nameRaw;
    int qtRaw;

    int idRaw;
    int PriceRaw;

    public String getNameRaw() {
        return nameRaw;
    }

    public void setNameRaw(String nameRaw) {
        this.nameRaw = nameRaw;
    }

    public int getQtRaw() {
        return qtRaw;
    }

    public void setQtRaw(int qtRaw) {
        this.qtRaw = qtRaw;
    }

    public int getIdRaw() {
        return idRaw;
    }

    public void setIdRaw(int idRaw) {
        this.idRaw = idRaw;
    }

    public int getPriceRaw() {
        return PriceRaw;
    }

    public void setPriceRaw(int priceRaw) {
        PriceRaw = priceRaw;
    }
}
